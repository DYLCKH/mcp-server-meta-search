#!/usr/bin/env bun
/**
 * Generates a small TypeScript module that embeds config.jsonc.example.
 * The server imports this module at compile time so single-file binaries can
 * write a starter config even when the source tree is not present.
 */
import { readFile, writeFile } from "node:fs/promises";
import { join, relative } from "node:path";

const ROOT = join(import.meta.dirname, "..");
const CONFIG_EXAMPLE = join(ROOT, "config.jsonc.example");
const OUT_FILE = join(
  ROOT,
  "apps",
  "server",
  "src",
  "config-example.generated.ts",
);

async function main() {
  const text = await readFile(CONFIG_EXAMPLE, "utf-8");
  const lines = [
    "// Auto-generated by scripts/embed-config-example.mjs - do not edit",
    "",
    `export const CONFIG_EXAMPLE = ${JSON.stringify(text)};`,
    "",
  ];

  await writeFile(OUT_FILE, lines.join("\n"), "utf-8");
  console.log(
    `[embed-config-example] Embedded ${relative(process.cwd(), CONFIG_EXAMPLE)} -> ${relative(process.cwd(), OUT_FILE)}`,
  );
}

main();
